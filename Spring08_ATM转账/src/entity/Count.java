package entity;

public class Count {
	private int count_id;
	private String count_name;
	private int count_money;
	public int getCount_id() {
		return count_id;
	}
	public void setCount_id(int countId) {
		count_id = countId;
	}
	public String getCount_name() {
		return count_name;
	}
	public void setCount_name(String countName) {
		count_name = countName;
	}
	public int getCount_money() {
		return count_money;
	}
	public void setCount_money(int countMoney) {
		count_money = countMoney;
	}
	
	

}
